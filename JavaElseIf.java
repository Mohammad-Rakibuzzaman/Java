package com.example.learnjava;

public class JavaElseIf {
    public static void main(String[] args) {

        char grade = 'E';

        if (grade == 'A') {
            System.out.println("A is for Awesome!!!");
        } else if (grade == 'B') {
            System.out.println("B is for Beautfully done!!!");
        } else if (grade == 'C'){
            System.out.println("C i for careless sorry to say.. :v");
        } else if (grade == 'F') {
            System.out.println("F is for future Unknown...");
        } else {
            System.out.println("Did you get D, E or G?");
        }






    }
}
